     void cargar_tabla_lista_completa(){
        
       
      
        
        try{
            //limpiar_tabla();
            
            String sql = "select distinct ficha_diagnostico.rut_alumno \"Rut del Alumno\" , alumno.nombres_alumno \"Nombres del Alumno\", alumno.apellido_paterno \"Apellido Paterno\" , alumno.apellido_materno \"Apellido Materno\", tipo_ficha.nombre_tipo \"Nombre Diagnostico\" , curso.nombre \"Curso Alumno\" from ficha_diagnostico , alumno , curso , tipo_ficha where ficha_diagnostico.rut_alumno = alumno.rut_alumno and ficha_diagnostico.id_tipoficha = tipo_ficha.id_tipo and ficha_diagnostico.curso_alumno = curso.id_curso and alumno.estado= 'activo'";
            ResultSet rs = con.ejecutarSQLSelect(sql);
            //String sq="select * from visfunc"; 
            String [] arregl = new String[7];
            
            while (rs.next()){
                
               
                
            arregl[0] = rs.getString(1);
            arregl[1] = rs.getString(2);
            arregl[2] = rs.getString(3);
            arregl[3] = rs.getString(4);
            arregl[4] = rs.getString(5); 
            arregl[5] = rs.getString(6); 
           
        model.addRow(arregl);
             
            }
            
            
        
        }catch(Exception ex){
            
            
        }
    }
      void limpiar_tabla(){
        DefaultTableModel tb = (DefaultTableModel) TablaInformeCompleto.getModel();
        int limpiar = TablaInformeCompleto.getRowCount()-1;
        for (int i = limpiar; i >= 0; i--) {           
        tb.removeRow(tb.getRowCount()-1);
        } 
      
    }

