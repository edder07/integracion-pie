    void cargar_combo_evaluador() { 
             
        try {
            String sql = "select profesional_evaluador.nombre_evaluador from profesional_evaluador where profesional_evaluador.estado = 'activo'";
            ResultSet rs = con.ejecutarSQLSelect(sql);
            comboevaluador.removeAllItems();
              
            while(rs.next()) {
                   comboevaluador.addItem(rs.getString("nombre_evaluador"));
               
            }
            } catch (SQLException ex) {
            }
        }


